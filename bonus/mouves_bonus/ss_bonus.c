#include "../checker_bonus.h"

void	swap_a_b(t_list **a, t_list **b)
{
	if ((!a || !(*a) || !(*a)-> next)
		|| (!b || !(*b) || !(*b)-> next))
		return ;
	swap_a(a);
	swap_b(b);
}
