#include "../push_swap.h"

void	ss(t_list **a, t_list **b)
{
	if ((!a || !(*a) || !(*a)-> next)
		|| (!b || !(*b) || !(*b)-> next))
		return ;
	sa(a, 0);
	sb(b, 0);
	write(1, "ss\n", 3);
}
