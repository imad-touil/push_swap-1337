#include "push_swap.h"

void	rr(t_list **a, t_list **b)
{
	if ((a && (*a) && (*a)-> next))
		ra(a, 0);
	if ((b && (*b) && (*b)-> next))
		rb(b, 0);
	write(1, "rr\n", 3);
}
